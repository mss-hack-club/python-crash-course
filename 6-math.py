# Math in python is pretty much exactly what you expect it to be

# make two integer variables
num = 3
num2 = 5

print(num + num2)  # this is addition
print(num - num2)  # this is subtraction
print(num * num2)  # this is multiplication
# this is exponentiation (num to the power of num2, 3 to the power of 5)
print(num ** num2)

# DIVISION - there are three types of division

# regular division - whole number and decimal
print(num / num2)

# integer division - drops the decimal and just gives the whole number
print(num // num2)

# remainder (modulus) - gives just the remainder
print(num % num2)
